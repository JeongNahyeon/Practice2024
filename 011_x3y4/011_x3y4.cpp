#include <stdio.h>

int main() {
    int x = 3;
    int y = 4;

    // 논리 연산자 결과 계산 및 출력
    printf("x && y = %d\n", x && y); // 논리 AND
    printf("x || y = %d\n", x || y); // 논리 OR
    printf("!x = %d\n", !x);         // 논리 NOT x
    printf("!y = %d\n", !y);         // 논리 NOT y

   
}



